#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	while (n-- && *(char *)s)
	{
		if (*(char *)s == c)
			return ((void *)s);
		s++;
	}
	if (n > 0 && c == 0 && *(char *)s == '\0')
		return ((void *)s);
	return (0);
}
